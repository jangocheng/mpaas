    private static final long seriaVersionUID = -8200601482506330634L;
    private Autowired autowired;
//private ModuleAutowired moduleAutowired;
    public Autowired getAutowired() {
        return autowired;
    }
    public ModuleBeanNotException(Autowired autowired) {
        super();
        this.autowired = autowired;
    }
    public ModuleBeanNotException(Autowired autowired, String message) {
        super(message);
        this.autowired = autowired;
    }
